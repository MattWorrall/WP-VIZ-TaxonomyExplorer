/**
 * Defines a font configuration.
 *
 * @date 2018-11-27
 * @export
 * @interface IFontConfig
 */
export interface IFontConfig {
  /**
   * The font family.
   *
   * @type {string}
   * @memberof IFontConfig
   */
  family: string;
  
  /**
   * The font size (in px).
   *
   * @type {number}
   * @memberof IFontConfig
   */
  size: number;
}